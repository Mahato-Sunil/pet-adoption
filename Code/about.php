        <div class="values">
            <h2>Our Values</h2>
            <ul>
                <li><strong>Compassion:</strong> We believe in treating all animals with kindness and empathy.</li>
                <li><strong>Reliability:</strong> We are committed to providing accurate and trustworthy information to
                    our audience.</li>
                <li><strong>Community:</strong> We strive to foster a supportive community where pet owners can connect
                    and learn from each other.</li>
                <li><strong>Education:</strong> We believe in empowering pet owners with knowledge to help them make
                    informed decisions about their pets' well-being.</li>
                <li><strong>Integrity:</strong> We conduct our work with honesty and integrity, prioritizing the welfare
                    of pets above all else.</li>
                <li><strong>Inclusivity:</strong> We welcome diversity and inclusivity within our community, respecting
                    the unique needs and perspectives of all pet owners.</li>
            </ul>
